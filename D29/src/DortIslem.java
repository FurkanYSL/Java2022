
public class DortIslem {
	public int Topla(int s1, int s2) {
		return s1 + s2;
	}

	public int Topla(int s1, int s2, int s3) {
		return s1 + s2 + s3;
	}

	public int Carp(int s1, int s2) {
		return s1 * s2;
	}

	public int Carp(int s1, int s2, int s3) {
		return s1 * s2 * s3;
	}

	public int Carp(int s1, int s2, int s3, int s4) {
		return s1 * s2 * s3 * s4;
	}
}
